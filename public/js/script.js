// This example uses the Google Maps JavaScript API's Data layer
// to create a rectangular polygon with 2 holes in it.

function initMap() {
  var map = new google.maps.Map(document.getElementById('map'), {
    //zoom: 5,
    //center: {lat: -0, lng: 0},
    zoom: 14,
    center: {lat: -36.844784, lng: 174.758700},
  });

  getData(map);
}
function getData(map){
  var xhr = new XMLHttpRequest();
  xhr.onload = function(){
    var d = JSON.parse(this.responseText);
    d = d.vectorQuery.layers;
    Object.keys(d).forEach(layer=>{
      d[layer].features.forEach(item=>{
        //console.log(Object.keys(item));
        var co = item.geometry.coordinates;
        co = co.map(c=>{
          //console.log(c);
          var cc = c.map(ll=>{
            return new google.maps.LatLng(ll[1],ll[0])
          })
          //console.log(cc);
          //console.log(JSON.stringify(cc));

          drawPolygon(cc, map);
          var polygon = new google.maps.Polygon({
              map: map,
              paths: cc,
              strokeColor: '#ff0000',
              strokeOpacity: 0.5,
              strokeWeight: 1,
              fillColor: '#ff0000',
              fillOpacity: 0.5
          });
          polygon.setOptions({
            fillColor: '#0000ff'
          });

          // STEP 3: Listen for clicks on the polygon.
          google.maps.event.addListener(polygon, 'click', function (event) {
              //alert('clicked polygon!');
          });
          // STEP 4: Listen for when the mouse hovers over the polygon.
          google.maps.event.addListener(polygon, 'mouseover', function (event) {
              // Within the event listener, "this" refers to the polygon which
              // received the event.
              this.setOptions({
                  strokeColor: '#00ff00',
                  fillColor: '#00ff00'
              });
          });
          // STEP 5: Listen for when the mouse stops hovering over the polygon.
          google.maps.event.addListener(polygon, 'mouseout', function (event) {
              this.setOptions({
                  strokeColor: '#ff0000',
                  fillColor: '#ff0000'
              });
          });

          return cc;
        });
        //console.log(JSON.stringify(co));
        //var marker = new google.maps.Marker({
        //  position: co[0][0],
        //  map: map,
        //  title: 'Hello World!'
        //});
      });
    });
  };
  xhr.open('GET', '/data');
  xhr.send();
}
